package day11;

public class c4_ForLoop {

    public static void main(String[] args) {

        //loops will help you on continuous actions
        //in the loop, you will always need to specify
                //---?starting point, ending point, and iteration.

        for (int i=0 ; i<=10; i++){
            //starting point is 0
            //ending point is 10
            //iteration is increase +1

            System.out.println("Java is fun");


        }

        for (int i=0; i<8; i+=2){

            System.out.println("Java is fun again?");
        }

        for (int k=1; k<=10; k+=3){
            System.out.println("Java");
        }


    }
}
