package day02;

public class c6_aritmeticOperators {

    public static void main(String[]args){

        /*
        float - 4 bytes stores fractional numbers, 6-7 decimals
        double - 8 bytes Stores fractional numbers, good to store 15 decimals

         */
        double d1=5.5;
        System.out.println(d1);

        double d2=2.33335245;

        System.out.println(d2);
        System.out.println(d1+d2);

        float f1=2.5f;

        System.out.println(f1);

        System.out.println(2+5);
        System.out.println(f1+d1);

        int int1=30;
        int int2=5;

        System.out.print(int1+int2);



    }


}
